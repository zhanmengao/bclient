void CMainFrame::OnNcLButtonDown(UINT nHitTest, CPoint point)
{
	// TODO: 在此添加消息处理程序代码和/或调用默认值
	if (nHitTest == HTMAXBUTTON)
	{
		AfxMessageBox(_T("最大化已不可用"));
		return;
	}
	else if (nHitTest == HTCLOSE)
	{
		AfxMessageBox(_T("关闭按钮已不可用，请用菜单文件|退出"));
		return;
	}
	CFrameWnd::OnNcLButtonDown(nHitTest, point);
}

//击中测试。系统判断鼠标事件发生时击中了哪里
LRESULT CMainFrame::OnNcHitTest(CPoint point)
{
	// TODO: 在此添加消息处理程序代码和/或调用默认值
	UINT nHitTest = CFrameWnd::OnNcHitTest(point);					//拿到系统判断的区域
	if (nHitTest == HTMENU)
	{
		//如果用户单击了菜单，则返回标题栏
		nHitTest = HTCAPTION;
	}
	return nHitTest;
}
